/**
 * Returns the hex value for a given Tailwind color palette and shade
 */
export function getColorHexValue(palette: string, shade: number): string {
  if (
    tailwindColorHexValues[palette] &&
    tailwindColorHexValues[palette][shade]
  ) {
    return tailwindColorHexValues[palette][shade];
  }
  return "#000000"; // Default to black if color not found
}

/**
 * Returns the appropriate Tailwind background class for a given color palette and shade
 */
export function getBgColorClass(palette: string, shade: number): string {
  return `bg-${palette}-${shade}`;
}

/**
 * Determines if text should be dark or light based on background shade
 * Generally, lighter shades (50-400) need dark text, darker shades need light text
 */
export function isLightColor(shade: number): boolean {
  return shade <= 400;
}

/**
 * Returns the appropriate text color class based on background shade
 */
export function getTextColorClass(shade: number): string {
  return isLightColor(shade) ? "text-gray-800" : "text-white";
}

